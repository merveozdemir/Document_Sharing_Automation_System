    public DosyaGoruntule(File f) throws IOException {
        initComponents();
        this.setTitle(f.getName());
        this.getContentPane().setBackground(new Color(215, 224, 240));
        file = new File(f.getAbsolutePath());
        String metin = "";
        BufferedImage img = null;

        if (file.getName().contains("txt") || file.getName().contains("dat")) {
            JTextArea textArea = new JTextArea();
            textArea.setSize(jPanel1.getSize());
            textArea.setLocation(jPanel1.getLocation());
            textArea.setEditable(false);
            JScrollPane sp = new JScrollPane();
            sp.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);
            sp.setBounds(20, 20, 650, 450);
            sp.getViewport().setBackground(Color.white);
            sp.getViewport().add(textArea);
            
            try {
                for (String line : readLines(file)) {
                    metin += line + "\n";
                }
            } catch (IOException x) {
                System.out.println(x);
            }
            textArea.setText(metin);
             jPanel1.add(sp);
            // jPanel1.add(textArea);
        } else if (file.getName().contains("png") || file.getName().contains("jpeg")
                || file.getName().contains("PNG") || file.getName().contains("JPG") || file.getName().contains("jpg")) {
                   
                   
            try {
                img = ImageIO.read(file);
                ImageDisplay p1 = new ImageDisplay();
                p1.setSize(jPanel1.getSize());
                p1.setLocation(jPanel1.getLocation());
                p1.setImage(img);
               
                jPanel1.add(p1);
            } catch (IOException ex) {
                System.out.println(" hata !! " + ex);
            }

        }
        

    }

    static String[] readLines(File f) throws IOException {

        InputStream is = new FileInputStream(f);
        byte[] ba = new byte[is.available()];
        is.read(ba);
        is.close();
        String str = new String(ba, "UTF-8");
        String[] sa = str.split("\n");
        System.out.printf("%s satir okundu %n", sa.length);
        return sa;
    }

